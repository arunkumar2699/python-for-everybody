# Grade to grade point mapping
grade_mapping = {
    "O": 4.0,
    "A+": 4.0,
    "A": 3.5,
    "B+": 3.0
}

# Extract subjects, credits, and grades
pattern = r"([A-Z0-9]+)\s+[\w\s&-]+\s+(\d+)\s+([OAB+]+)"
matches = re.findall(pattern, marksheet_text)

# Initialize variables for GPA calculation
total_grade_points = 0
total_credits = 0

# Process each match
for subject, credit, grade in matches:
    credit = int(credit)
    grade_point = grade_mapping.get(grade, 0)  # Default to 0 if grade not found
    total_grade_points += credit * grade_point
    total_credits += credit

# Calculate GPA
us_gpa = total_grade_points / total_credits if total_credits else 0
